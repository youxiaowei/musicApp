<template>
  <div class="recommend" ref="recommend">
    <scroll ref="scroll" class="recommend-content" :data="discList">
      <div>
        <div v-if="recommends.length" class="slider-wrapper">
          <slider>
            <div v-for="item in recommends" :key="item.id">
              <a :href="item.linkUrl">
                <!-- 加一个class，needsclick，是告诉fastclick，这个需要的点击，那么就不会拦截点击 -->
                <img class="needsclick" @load="loadImage" :src="item.picUrl" />
              </a>
            </div>
          </slider>
        </div>
        <div class="recommend-list">
          <h1 class="list-title">热门歌单推荐</h1>
          <ul>
            <li v-for="item in discList" class="item" :key="item.id">
              <div class="icon">
                <img v-lazy="item.picUrl" width="60" height="60"/>
              </div>
              <div class="text">
                <h2 class="name">{{item.songListDesc}}</h2>
                <p class="desc">{{item.songListAuthor}}</p>
              </div>
            </li>
          </ul>
        </div>
      </div>
      <div class="loading-container" v-show="!discList.length">
        <loading></loading>
      </div>
    </scroll>
  </div>
</template>

<script type="text/ecmascript-6">
import Loading from '../../base/loading/loading'
import Scroll from '../../base/scroll/scroll'
import Slider from '../../base/slider/slider'
import {getRecommend} from '../../api/recommend'
import {ERR_OK} from '../../api/config'
export default {
  data () {
    return {
      recommends: [],
      discList: [],
      scrollList: []
    }
  },
  created () {
    this._getRecommend()
    this._getDiscList()
  },
  methods: {
    _getRecommend () {
      getRecommend().then((res) => {
        if (res.code === ERR_OK) {
          console.log(res.data.slider)
          this.recommends = res.data.slider
        }
      })
    },
    _getDiscList () {
      getRecommend().then((res) => {
        if (res.code === ERR_OK) {
          console.log(res.data.songList)
          this.discList = res.data.songList
        }
      })
    },
    // 判断是否轮播图已加载，来撑开盒子，来刷新scroll的高度，且只需要潘墩一张已加载就好了
    loadImage () {
      if (!this.checkLoaded) {
        this.$refs.scroll.refresh()
        this.checkLoaded = true
      }
    }
  },
  components: {
    Slider,
    Scroll,
    Loading
  }
}
</script>

